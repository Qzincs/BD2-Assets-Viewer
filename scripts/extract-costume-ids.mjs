import fs from 'node:fs'
import path from 'node:path'

const projectRoot = process.cwd()

const inventoryDir = path.join(
  projectRoot,
  'public',
  'assets',
  'illust',
  'inventory',
)

const outputDir = path.join(projectRoot, 'src', 'data', 'generated')
const outputFile = path.join(outputDir, 'costumeIds.ts')

function parseCostumeFileName(fileName) {
  const match = fileName.match(/^(char)(\d{6})\.png$/i)
  if (!match) return null

  const numericId = match[2]
  const costumeId = `char${numericId}`

  // We don't want these (summoned and a security guard)
  if (numericId.startsWith('05') || numericId.startsWith('70')) return null

  // Celia has two different character IDs, map both to char0604
  const characterId = numericId.startsWith('1016') ? 'char0604' : `char${numericId.slice(0, 4)}`

  const costumeSuffix = numericId.slice(4, 6)
  const isPrestige = costumeSuffix[0] === '9'

  const baseCostumeId = isPrestige
    ? `char${numericId.slice(0, 4)}0${costumeSuffix[1]}`
    : costumeId

  return { costumeId, characterId, costumeSuffix, isPrestige, baseCostumeId }
}

function main() {
  if (!fs.existsSync(inventoryDir)) {
    console.error(`Inventory directory not found: ${inventoryDir}`)
    process.exit(1)
  }

  const files = fs.readdirSync(inventoryDir)

  const costumes = files
    .map(parseCostumeFileName)
    .filter(Boolean)
    .sort((a, b) => a.costumeId.localeCompare(b.costumeId))

  const characterIds = [...new Set(costumes.map((item) => item.characterId))].sort()

  fs.mkdirSync(outputDir, { recursive: true })

  const content = `// This file is generated by scripts/extract-costume-ids.mjs.
// Do not edit manually.

export type GeneratedCostumeId = {
  costumeId: string
  characterId: string
  costumeSuffix: string
  isPrestige: boolean
  baseCostumeId: string
}

export const generatedCharacterIds = ${JSON.stringify(characterIds, null, 2)} as const

export const generatedCostumeIds: GeneratedCostumeId[] = ${JSON.stringify(costumes, null, 2)}
`

  fs.writeFileSync(outputFile, content, 'utf-8')

  console.log(`Found ${characterIds.length} characters`)
  console.log(`Found ${costumes.length} costumes`)
  console.log(`Generated: ${path.relative(projectRoot, outputFile)}`)
}

main()